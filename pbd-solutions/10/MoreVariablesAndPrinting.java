public class MoreVariablesAndPrinting {
    public static void main(String[] args) {
        String name, eyes, teeth, hair;
        double age, height, weight;
        
        name = "Ito Palaganas";
        age = 16; 
        height = 63 * 2.54; //inches to centimeter
        weight= 100 * 0.453592; //lbs to kg
        eyes = "Brown";
        teeth = "White";
        hair = "Black";
        
        System.out.println("Let's talk about " + name + ".");
        System.out.println("He's " + height + " centimeters tall.");
        System.out.println("He's " + weight + " kilograms heavy.");
        System.out.println("Actually, that's not too heavy.");
        System.out.println("He's teeth are usually " + teeth + " depending on the coffee.");
        
        System.out.println("If I add " + age + ", " + height + ", and " + weight + " I get " + (age + height + weight) + ".");
        }
}
